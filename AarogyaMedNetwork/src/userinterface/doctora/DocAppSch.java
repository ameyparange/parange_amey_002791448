public void initializetab(Patient p)
{   
    DefaultTableModel model = (DefaultTableModel) jtencounter.getModel();
        model.setRowCount(0);

    for (VitalSigns vs : enc.getEncounter()){
        if (p.getPatient_id()==vs.getPatient_id())
        {
            
        

            Object[] row = new Object[10];
            row[0] = vs.getVitalsignsid();
            row[1] = vs.getTimestamp();//username
            row[2] = vs.getComm_name();
            
            row[3] = vs.getPulserate();
            row[4] = vs.getBloodpressure();
            row[5] = vs.getSugerlevel();
            row[6] = vs.getTemperature();
            row[7] = vs.getSymtoms();
            
            model.addRow(row);
            
         
    
    }
    
}
}

