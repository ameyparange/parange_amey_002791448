    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
       // btnedit.setEnabled(true);
       // disableViewfield();
        //Patient updpat= new Patient();
        String tpatid = tfpatid.getText();
        //int taddid = hosdir.searchIndexhospital(Integer.valueOf(thosid)).getAddressid();
        String tfenco = tfencno.getText();
        String tdate = tfdate.getText();
        String tcomm= tfcomm.getText();
        //String tdesc = tfdesc.getText();
        String tbp = tfbp.getText();
        String tsl = tfsl.getText();
        String tpr = tfpr.getText();
        String tsym = tfsym.getText();
        String tbt = tfbt.getText();
        

        boolean er = false;

        Validations val = new Validations();
        try {

            if (tfenco.isEmpty() || tpatid.isEmpty() || tdate.isEmpty() || tcomm.isEmpty() || tbp.isEmpty()
                || tsl.isEmpty() || tpr.isEmpty() || tsym.isEmpty() || tbt.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Cannot enter empty field",
                    "Try Again",
                    JOptionPane.ERROR_MESSAGE);
            } else {
                //Employee emp = new Employee();
                VitalSigns vss= new VitalSigns();
                //hos.setHospitalid(9999);
                //add.setAddress_id(9999);
                vss.setVitalsignsid(9999);
                if (val.valhospname(tcomm) == false) {
                    JOptionPane.showMessageDialog(this, "Please Enter Community Name in Proper format",
                        "Try Again",
                        JOptionPane.ERROR_MESSAGE);
                    er = true;
                } else {
                    vss.setComm_name(tcomm);
                    vss.setPatient_id(Integer.valueOf(tpatid));
                    
                    vss.setTimestamp(tdate);
                }
                if (!val.valnum(tsl)||!val.valnum(tbp)||!val.valnum(tpr)||!val.valnum(tbt)) {
                    JOptionPane.showMessageDialog(this, "Please Enter Valid Values",
                        "Try Again",
                        JOptionPane.ERROR_MESSAGE);
                    er = true;
                } else {
                    vss.setBloodpressure(tbp);
                    vss.setPulserate(tpr);
                    vss.setSugerlevel(tsl);
                    vss.setTemperature(tbt);
                    vss.setSymtoms(tsym);
                    // pat.setMobileno(Integer.valueOf(tzipcode));
                }

                if (er != true) {

                    //catalog.addnewemployee(emp);
                    vss.setVitalsignsid(Integer.valueOf(tfenco));
                    enc.updateencounter(vss, Integer.valueOf(tfenco));

                    JOptionPane.showMessageDialog(this, "Encounter Successfully Updated!!!");

                    this.initializetab(patdir.searchIndexPat(vss.getPatient_id()));
                } else {

                    //catalog.deleteemprec(0);
                    hosdir.deletehospitalrec(9999);
                    community.deleteaddressrec(9999);
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e + "Please Enter Valid Values",
                "Try Again",
                JOptionPane.ERROR_MESSAGE);
            hosdir.deletehospitalrec(9999);
            community.deleteaddressrec(9999);
        }
    }//GEN-LAST:event_btnupdateActionPerformed

