    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        String query;
        Communnity hos = new Communnity();
        if (!tfnameser.getText().isEmpty()) {
            query = tfnameser.getText();
            for (Communnity emp1 : c.getcc()) {
                if (emp1.getName().equalsIgnoreCase(query)) {
                    hos = emp1;
                }
            }
        } else if (!tfcityser.getText().isEmpty()) {
            query = tfcityser.getText();
            for (Communnity emp1 : c.getcc()) {

                if (emp1.getName().equalsIgnoreCase(query)) {
                    hos = emp1;
                }
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Search on Community Name or City");
        }
        if (hos != null) {
            JOptionPane.showMessageDialog(this,
                    "Hospital Found!!!");
            populatetable(hos);
            
        } else
            JOptionPane.showMessageDialog(this,
                    "Hospital does not exist!!!");
    }//GEN-LAST:event_btnsearchActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        
        int row = jTcomm.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this,
                    "No row is selected! Please select one row",
                    "Select row",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            DefaultTableModel model = (DefaultTableModel) jTcomm.getModel();

            String selected_name = (String) model.getValueAt(row, 0);
            String selected_city = (String) model.getValueAt(row, 1);
            int selected_hosid = (int) model.getValueAt(row, 2);
            c.deletecommrec(selected_name,selected_city,selected_hosid);
            //addressdelete
            populatetable();}
    }//GEN-LAST:event_btndeleteActionPerformed


