private void populatetable() {
        DefaultTableModel model = (DefaultTableModel) jTcomm.getModel();
        model.setRowCount(0);int i = 0;
        for (Communnity hos : c.getcc()) {

            Object[] row = new Object[5];
            row[0] = hos.getName();
            row[1] = hos.getCity();//username
            row[2] = hos.getHos_id();

            model.addRow(row);

            i++;
            //JOptionPane.showMessageDialog(this, "Successfully Employee Profile Created!!!");
        }

        

    }

